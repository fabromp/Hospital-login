    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ci = txtCI.getText();
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String usuario = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        String gruposanguineo = txtGruposanguineo.getText();
        String enfermedad = txtEnfermedad.getText();
        int posPaciente = Paciente.validarUsuarioPaciente(usuario, contraseña);
        int posUbi = Paciente.buscarPaciente(ci);
        if (posPaciente == -1 && posUbi==-1) {
            Paciente obj = new Paciente();
            obj.setCi(ci);
            obj.setNombre(nombre);
            obj.setApellidos(apellidos);
            obj.setUsuario(usuario);
            obj.setContraseña(contraseña);
            obj.setGruposanguineo(gruposanguineo);
            obj.setEnfermedad(enfermedad);
            ListaPacientes.agregar(obj);
            Icon imagen=new ImageIcon(getClass().getResource("/Iconos/personaspeque.png"));
            JOptionPane.showMessageDialog(this, "SE REGISTRO EL PACIENTE CON EXITO","Paciente",JOptionPane.INFORMATION_MESSAGE,imagen);
            mostrarListado();
        } else {
            JOptionPane.showMessageDialog(this, "YA EXISTE EL PACIENTE");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

