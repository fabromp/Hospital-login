    private void mostrarListado() {
      
        Vector listaPaciente= ListaPacientes.mostrar();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("CI");
        model.addColumn("Nombre");
        model.addColumn("Apellidos");
        model.addColumn("Usuario");
        model.addColumn("Contraseña");
        model.addColumn("Grupo S");
        model.addColumn("Enfermedad");
        Object[] datos = new Object[7];

        for (int i = 0; i < listaPaciente.size(); i++) {
           Paciente obj = (Paciente) listaPaciente.elementAt(i);
            datos[0] = obj.getCi();
            datos[1] = obj.getNombre();
            datos[2] = obj.getApellidos();
            datos[3] = obj.getUsuario();
            datos[4] = obj.getContraseña();
            datos[5] = obj.getGruposanguineo();
            datos[6] = obj.getEnfermedad();
     model.addRow(datos);
        }
        jTable1.setModel(model);
    }
