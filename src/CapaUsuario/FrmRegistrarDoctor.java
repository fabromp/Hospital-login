    private void mostrarListado() {
        Vector listaDoctores = ListaDoctores.mostrar();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("CI");
        model.addColumn("Nombre");
        model.addColumn("Apellidos");
        model.addColumn("Usuario");
        model.addColumn("Contraseña");
        model.addColumn("Especialidad");
        Object[] datos = new Object[6];

        for (int i = 0; i < listaDoctores.size(); i++) {
            Doctor obj = (Doctor) listaDoctores.elementAt(i);
            datos[0] = obj.getCi();
            datos[1] = obj.getNombre();
            datos[2] = obj.getApellidos();
            datos[3] = obj.getUsuario();
            datos[4] = obj.getContraseña();
            datos[5] = obj.getEspecialidad();
     model.addRow(datos);
        }
        jTable1.setModel(model);
    }
