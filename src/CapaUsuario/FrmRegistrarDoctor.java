    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ci = txtCI.getText();
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String usuario = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        String especialidad=txtEspecialidad.getText();
        int posDoctor = Doctor.validarUsuarioDoctor(usuario, contraseña);
        int posUbi = Doctor.buscarDoctor(ci);
        if (posDoctor == -1 && posUbi==-1) {
            Doctor obj = new Doctor();
            obj.setCi(ci);
            obj.setNombre(nombre);
            obj.setApellidos(apellidos);
            obj.setUsuario(usuario);
            obj.setContraseña(contraseña);
            obj.setEspecialidad(especialidad);
            ListaDoctores.agregar(obj);
            Icon imagen=new ImageIcon(getClass().getResource("/Iconos/doctorpeque.png"));
            JOptionPane.showMessageDialog(this, "SE REGISTRO EL DOCTOR CON EXITO","Doctore",JOptionPane.INFORMATION_MESSAGE,imagen);
            mostrarListado();
        } else {
            JOptionPane.showMessageDialog(this, "YA EXISTE EL DOCTOR");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

